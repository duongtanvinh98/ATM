    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      if(MaTKNhan()==0){
          JOptionPane.showMessageDialog(null,"Tài khoản không hợp lệ");
      }
      else {
        if(sotienchuyen()==0){
             JOptionPane.showMessageDialog(null,"Số tiền không hợp lệ", "Thông báo",1);
             txtSotienchuyen.setText("");
         }
         else{
             if(ktrMaxacnhan()==false) {
                JOptionPane.showMessageDialog(null,"Mã xác nhận chưa đúng", "Thông báo",1);
                String code= String.valueOf(random());
                txtRdcode.setText(code);
                txtCode.setText("");
             }
             else{
                int tkchuyen,tknhan;
                double tienchuyen;
                tkchuyen=Integer.parseInt(txtMaTKChuyen.getText());
                tknhan=MaTKNhan();
                    tienchuyen=sotienchuyen();
                    Deal dl = new Deal();
                    try {
                        dl.ChuyenTien(tkchuyen, tknhan, tienchuyen);
                        if(dl!=null){
                             JOptionPane.showMessageDialog(null,"Đã chuyển tiền thành công", "Thông báo",1);
                             txtSotienchuyen.setText("");
                             txtCode.setText("");
                             int n = JOptionPane.showConfirmDialog(null,"Tiếp tục thực hiện các giao dịch ?", "Cảnh báo",JOptionPane.YES_NO_OPTION);
                             if(n == JOptionPane.YES_OPTION)
                                   ATMSystem.showPanel(new Wellcom());
                             else if(n==JOptionPane.NO_OPTION){
                                       ATMSystem.account=0;
                                       JOptionPane.showMessageDialog(null, "Bạn đã đăng xuất");
                                       ATMSystem.showPanel(new Login());   
                                   }
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(ChuyenTien.class.getName()).log(Level.SEVERE, null, ex);
                    }
             } 
         }
      }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private int rand(int min,int max){
        try {
                Random rn = new Random();
                int range = max - min + 1;
                int randomNum = min + rn.nextInt(range);
                return randomNum;
            } catch (Exception e) {
                e.printStackTrace();
                return -1;
            }
     }
    private int random(){
          int rand = rand(1000,9000);
          return rand;
    }
