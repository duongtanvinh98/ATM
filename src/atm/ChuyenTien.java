    private int ktrAmount(String ktr){
        int kq=0;
        try 
        { 
            String regex = "^\\d+$"; 
            if(ktr.matches(regex)) 
            { 
                kq=1;
            } 
        } 
        catch(Exception ex) 
        { 
            throw ex; 
        } 
        return kq;
    }
    private boolean ktrMaxacnhan(){
        int code = Integer.parseInt(txtCode.getText());
        int code1=Integer.parseInt(txtRdcode.getText());
        if(code == code1){
                return true;
            }
            return false;
    } 
    private void TaiKhoan(){
        int accode = ATMSystem.account;
        try {
            Account acc = new Account(accode);
            txtMaTKChuyen.setText(String.valueOf(acc.getAccountid()));
            txtSodu.setText(String.valueOf(acc.getBalance()));
        } catch (SQLException ex) {
            Logger.getLogger(ChuyenTien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private int MaTKNhan(){
        int mtk=0;
        String tk=cbMatkNhan.getSelectedItem().toString().substring(1,5);
        mtk=Integer.parseInt(tk);
        if(mtk == ATMSystem.account){
            mtk=0;
        }
        return mtk; 
    }
    private double sotienchuyen(){
        double tien,tienchuyen;
        String sotien=txtSotienchuyen.getText();
        if(ktrAmount(sotien)!=0){
            tienchuyen=Double.parseDouble(sotien);
            tien=Double.parseDouble(txtSodu.getText());
            if(tienchuyen >= tien || tienchuyen <10 ){
                tienchuyen = 0;
            }
            else tienchuyen=Double.parseDouble(sotien);
        }
        else tienchuyen=0;
        return tienchuyen;
    }
