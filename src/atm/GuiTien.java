     private int ktrAmount(String ktr){
        int kq=0;
        try 
        { 
            String regex = "^\\d+$"; 
            if(ktr.matches(regex)) 
            { 
                kq=1;
            } 
        } 
        catch(Exception ex) 
        { 
            throw ex; 
        } 
        return kq;
    }
    private boolean ktrMaxacnhan(){
        int code = Integer.parseInt(txtCode.getText());
        int code1=Integer.parseInt(txtRdcode.getText());
        if(code == code1){
                return true;
            }
            return false;
    } 
    private void btnGuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuiActionPerformed
        Date today = new Date(System.currentTimeMillis());
        SimpleDateFormat timeFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss ");
        String time = timeFormat.format(today.getTime());
        String ktr=txtSotien.getText();
        if(ktrAmount(ktr)==0 || ktr==null){
                JOptionPane.showMessageDialog(null,"Số tiền không hợp lệ", "Thông báo",1);
        }
        else {
            double amount=Double.parseDouble(ktr);     
            Deal dl=new Deal();
            if(ktrMaxacnhan()==true){       
                try {
                    dl.SendAmount(ATMSystem.account, "Gửi tiền", "Thành công", amount);
                    if(dl!=null){
                        JOptionPane.showMessageDialog(null,"Đã gửi tiền thành công vào lúc "+time, "Thông báo",1);
                        txtSotien.setText("");
                        txtCode.setText("");
                        int n = JOptionPane.showConfirmDialog(null,"Tiếp tục thực hiện các giao dịch ?", "Cảnh báo",JOptionPane.YES_NO_OPTION);
                        if(n == JOptionPane.YES_OPTION)
                               ATMSystem.showPanel(new Wellcom());
                        else if(n==JOptionPane.NO_OPTION){
                                   ATMSystem.account=0;
                                   JOptionPane.showMessageDialog(null, "Bạn đã đăng xuất");
                                   ATMSystem.showPanel(new Login());   
                               }
                    }
                    else JOptionPane.showMessageDialog(null,"Nạp tiền không thành công", "Thông báo",1);
                }
                catch (SQLException ex) {
                    Logger.getLogger(GuiTien.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else {
                JOptionPane.showMessageDialog(null,"Mã xác nhận chưa đúng", "Thông báo",1);
                String code= String.valueOf(random());
                txtRdcode.setText(code);
                txtCode.setText("");
            }
        }  
    }//GEN-LAST:event_btnGuiActionPerformed
  
