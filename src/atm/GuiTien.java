    private int rand(int min,int max){
        try {
                Random rn = new Random();
                int range = max - min + 1;
                int randomNum = min + rn.nextInt(range);
                return randomNum;
            } catch (Exception e) {
                e.printStackTrace();
                return -1;
            }
     }
    private int random(){
          int rand = rand(1000,9000);
          return rand;
    }

